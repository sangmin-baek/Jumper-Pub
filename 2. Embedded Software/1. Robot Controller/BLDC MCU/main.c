/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MPU Configuration--------------------------------------------------------*/
  MPU_Config();

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_SPI2_Init();
  MX_TIM1_Init();
  MX_TIM2_Init();
  MX_TIM7_Init();
  MX_TIM4_Init();
  MX_UART4_Init();
  MX_SPI1_Init();
  MX_ADC3_Init();
  MX_TIM5_Init();
  MX_UART9_Init();
  /* USER CODE BEGIN 2 */

  /**
    ***************************************
    * micro seconds timer init
    ***************************************
    */
  HAL_TIM_Base_Start(&htim5);


  /**
    ***************************************
    * BLDC CMD Read init
    ***************************************
    */
  Init_BLDC_CMD_UART(&huart9);
  Start_BLDC_CMD_UART_RX_DMA();


  /**
    ***************************************
    * Position Read init
    ***************************************
    */
  Init_SPI_AS5047P(&hspi1, Mag_Encoder_SPI1_nCS_GPIO_Output_GPIO_Port, Mag_Encoder_SPI1_nCS_GPIO_Output_Pin);
  AS5047P_Set_Factory_Settings();
  AS5047P_Setting_ABZ();


  /**
    ***************************************
    * BLDC Motor init
    ***************************************
    */
  BLDC_Motor_Init(&htim2);

  /**
    ***************************************
    * Current Read init
    ***************************************
    */
  DRV8311_Current_Sensor_Init(&hadc3);


  /**
    ***************************************
    * BLDC Motor FOC init
    ***************************************
    */

  BLDC_Motor_Control_Init();



  /**
    ***************************************
    * Position Read init & Zero set
    ***************************************
    */

  AS5047P_Set_Zero_Position();
  AS5047P_Sensing_Init();

  //HAL_TIM_Encoder_Start(&htim1, TIM_CHANNEL_ALL);
  Init_ABZ_AS5047P(&htim1);



  /**
    ***************************************
    * LPF init
    ***************************************
    */
  LPF_Init();


  /**
    ***************************************
    * BLDC data init
    ***************************************
    */
  Init_BLDC_Data_SPI_Driver(&hspi2);
  Start_BLDC_TX_SPI_DMA();



  /**
    ***************************************
    * BLDC Motor init
    ***************************************
    */



  /**
    ***************************************
    * F. Main loop IT on
    ***************************************
    */
  HAL_TIM_Base_Start_IT(&htim7);


  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  if(tim7_flag == 1)
	  {
		  tim7_flag = 0;

		  /**
		    ***************************************
		    * 1. Cnt
		    ***************************************
		    */
		  tim7_cnt++;
		  bldc_cmd_cnt++;
		  position_read_cnt++;
		  current_read_cnt++;
		  bldc_data_cnt++;
		  bldc_ctrl_cnt++;


		  /**
		    ***************************************
		    * Position Read
		    ***************************************
		    */
		  if(position_read_cnt == POSITION_READ_PERIOD)
		  {
			  position_read_cnt = 0;
			  AS5047P_Get_Raw_Angle_Velocity_ABZ();
			  LPF_ANGLE();
			  LPF_VELOCITY();
			  BLDC_Motor_Get_States();
		  }

		  /**
		    ***************************************
		    * Current Read
		    ***************************************
		    */
		  if(current_read_cnt == CURRENT_READ_PERIOD)
		  {
			  current_read_cnt = 0;
			  //current dma data to current
			  DRV8311_Current_Sensor_Get_DQ_Current(_bldc_motor.angle_electrical);
			  LPF_Current_D();
			  LPF_Current_Q();
		  }

		  /**
		    ***************************************
		    * BLDC Data Tx update
		    ***************************************
		    */
		  if(bldc_data_cnt == BLDC_DATA_TX_PERIOD)
		  {

			  bldc_data_cnt = 0;
			  Form_BLDC_DATA_TX_Buff();

		  }

		  /**
		    ***************************************
		    * BLDC CTRL
		    ***************************************
		    */
		  if(bldc_ctrl_cnt == BLDC_CTRL_PERIOD)
		  {
			  bldc_ctrl_cnt = 0;
			  //if(tim7_cnt > 20)
			  {
				  BLDC_Motor_Controller(tim7_cnt);
				  HAL_GPIO_TogglePin(LED_GPIO_Output_GPIO_Port, LED_GPIO_Output_Pin);
			  }

		  }

	  }
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

